void LCD_P6x8Value(unsigned short x, unsigned short y, unsigned short fc, unsigned short bc,int data)
{
	unsigned short val[5],k; 
	static unsigned Show_5Bits = 0;
	if(data < 0)
	{
		LCD_P6x8Char(x,y,fc,bc,'-');
		data = - data;	
	}
	else
	{
		LCD_P6x8Char(x,y,fc,bc,' ');
	}
	if(data>100000)   //显示数大小溢出！！
	{
		LCD_P6x8Str(x,y,fc,bc,"Over!!");
	}
	else if(data >1000)             // 5位数显示
	{
		Show_5Bits = 1;  
		val[0] = (data%100000)/10000;
		val[1] = (data%10000)/1000;
		val[2] = (data%1000)/100;
		val[3] = (data%100)/10;
		val[4] = data%10;
		if(val[0]!=0) 	LCD_P6x8Char(x+6,y,fc,bc,val[0]+48);else LCD_P6x8Char(x+6,y,fc,bc,' ');
		if(val[1]!=0||(val[1]==0&&val[0]!=0)) LCD_P6x8Char(x+12,y,fc,bc,val[1]+48);else LCD_P6x8Char(x+12,y,fc,bc,' ');
		
			for(k=2;k<5;k++)
			{
				LCD_P6x8Char(x+6*(k+1),y,fc,bc,val[k]+48); 
			}
	}
	else     //  3位数显示
	{
		if(Show_5Bits == 1)
		{
			Show_5Bits = 0;
			LCD_P6x8Char(x+24,y,fc,bc,' ');
			LCD_P6x8Char(x+30,y,fc,bc,' ');
		}
		val[0] = (data%1000)/100;
		val[1] = (data%100)/10;
		val[2] = data%10;
		
		if(val[0]!=0) LCD_P6x8Char(x+6,y,fc,bc,val[0]+48);else LCD_P6x8Char(x+6,y,fc,bc,' ');
		if(val[1]!=0||(val[1]==0&&val[1]!=0))LCD_P6x8Char(x+12,y,fc,bc,val[1]+48);else LCD_P6x8Char(x+12,y,fc,bc,' ');
		LCD_P6x8Char(x+18,y,fc,bc,val[2]+48);
		
	}		
}

